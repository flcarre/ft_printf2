#include "ft_printf.h"

int		ft_wprocess3(t_id *e, unsigned long long x)
{
	long	r[2];

	r[0] = 0;
	r[1] = 0;
	e->w += (e->id[0] == 'x' && x == 0 && (e->fm & 16) == 16 && \
	e->p[0] == 0 && e->w > 0) ? 1 : 0;
	if ((e->fm & 8) != 8 || ((e->fm & 8) == 8 && (e->fm & 4) == 4) || e->p[1])
		((r[1] = ft_iputxchar(' ', e->w)) >= 0) ? (r[0] += r[1]) : 0;
	return ((int)((r[1] < 0) ? -1 : r[0]));
}
